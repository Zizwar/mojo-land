create table
  public.mojos (
    id bigint generated by default as identity,
    created_at timestamp with time zone null default now(),
    updated_at timestamp with time zone null,
    updated_by bigint null,
    status text null,
    title_ar text null,
    title_en text null,
    columns text null,
    "table" text null,
    prefix text null,
    endpoint text null,
    comment text null,
    data jsonb null,
    permissions jsonb null,
    sql text null,
    function text null,
    role text null,
    method text null,
    "select" text null,
    uuid uuid null default gen_random_uuid (),
    rpc text null,
    single boolean null,
    log boolean null,
    filters json null,
    text text null,
    methods jsonb null,
    name text null,
    is_active boolean null,
    selects text null,
    files jsonb null,
    embedding text null,
    jwt jsonb null,
    "userId" bigint null,
    version text null default '1.1'::text,
    checker boolean null default false,
    constraint mojo_pkey primary key (id),
    constraint mojos_endpoint_key unique (endpoint),
    constraint public_mojos_updated_by_fkey foreign key (updated_by) references users (id),
    constraint public_mojos_userid_fkey foreign key ("userId") references users (id)
  ) tablespace pg_default;